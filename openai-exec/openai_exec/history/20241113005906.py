# Introduction: The following code generates a list of even numbers between 1 and 20.
# The code uses a list comprehension to iterate through a range of numbers and filter out the odd numbers.

# Improved Code: In this version, I will generate a list of odd numbers between 1 and 20 in a creative way using list comprehension.

# Generate a list of odd numbers between 1 and 20
odd_numbers = [num for num in range(1, 21) if num % 2 != 0]

# Print the list of odd numbers
print(odd_numbers)
