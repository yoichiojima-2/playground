# Original Python script to print "Hello, World!"
print("Hello, World!")

# Added a creative twist by printing each character of the message in a separate line
for char in "Hello, World!":
    print(char)

# Changed the message to be uppercase
print("HELLO, WORLD!")

# Reversed the message
print("Hello, World!"[::-1])

# Replaced space with dash in the message
print("Hello,-World!")

# Converted the string into a list of words and printed each word on a separate line
words = "Hello, World!".split()
for word in words:
    print(word)
